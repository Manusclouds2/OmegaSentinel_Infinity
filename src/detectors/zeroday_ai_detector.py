    def _calculate_entropy(self, file_path: str) -> float:
        try:
            with open(file_path, "rb") as f:
                data = f.read()
            if not data: return 0.0
            entropy = 0
            for i in range(256):
                p_i = data.count(i) / len(data)
                if p_i > 0:
                    entropy -= p_i * math.log2(p_i)
            return entropy
        except:
            return 0.0

    def _run_ai_heuristics(self, file_path: str) -> float:
        score = 0.0
        try:
            file_size = os.path.getsize(file_path)
            if file_size < 1024 * 100: score += 0.2
            with open(file_path, "rb") as f:
                header_data = f.read(4096)
                if header_data.startswith(b"MZ"): score += 0.1
                suspicious_patterns = [b"powershell", b"base64", b"virtualalloc", b"reverse_tcp"]
                for pattern in suspicious_patterns:
                    if pattern in header_data.lower(): score += 0.15
        except Exception as e:
            logger.error(f"Heuristic error: {e}")
        return min(1.0, score)

